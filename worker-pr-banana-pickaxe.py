    pag.write('rpg craft banana 100')
    pag.enter('enter')
    time.sleep(random.randint(1,2))
    pag.write('rpg craft epic log 60000')
    pag.enter('enter')
    time.sleep(random.randint(1,2))
    pag.write('rpg craft super log 6000')
    pag.enter('enter')
    time.sleep(random.randint(1,2))
    pag.write('rpg craft mega log 600')
    pag.enter('enter')
    time.sleep(random.randint(1,2))
    pag.write('rpg cook banana pickaxe 100')
    pag.enter('enter')
    time.sleep(random.randint(1,2))